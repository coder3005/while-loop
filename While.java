/* While Loop Example...! */
import java.util.Scanner;
class While
{
    public static void main(String[] args) {

        int n;
        System.out.println("Enter value for Condition");
        Scanner ref=new Scanner(System.in);
        n=ref.nextInt();

        while(n>=0)
        {
            System.out.print("Learn Coding");
        }
    }
}
